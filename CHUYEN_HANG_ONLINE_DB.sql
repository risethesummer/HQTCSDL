GO

ALTER TABLE DOI_TAC ALTER COLUMN SO_CHI_NHANH INT
GO

CREATE TABLE HOP_DONG
(
	MA_HD VARCHAR(10),

	MA_THUE_DT VARCHAR(15),
	NGUOI_DAI_DIEN NVARCHAR(30),
	MA_CN_DK VARCHAR(8),
	DIA_CHI_CAC_CN NVARCHAR(30),
	NGAY_BD DATE,
	NGAY_KT DATE,
