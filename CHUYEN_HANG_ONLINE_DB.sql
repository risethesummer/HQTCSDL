(
	MA_DT CHAR(8) NOT NULL,

	TEN_DT NVARCHAR(20),
	NGUOI_DAI_DIEN NVARCHAR(30),
	THANH_PHO NVARCHAR(15),
	QUAN NVARCHAR(15),
	DIA_CHI_KINH_DOANH NVARCHAR(30),
